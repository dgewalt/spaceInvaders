package main.java;

public class Dimension2D {

    private final double width;
    private final double height;

    public Dimension2D(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return this.width;
    }

    public double getHeight() {
        return this.height;
    }
}
